package DataTypes_Abstraction_Interface;

public class DataTypes {

	public static void main(String[] args) {

		// byte will accept only -128 to 127
		byte a = 23;
		System.out.println(a);
		
		// Short will accept only -32768 to 32767
		short b = 34;
		System.out.println(b);
		
		//double will accept decimal number of 15 digits. 
		double s = 1254.8754327;
		System.out.println(s);
		
		// output for boolean data is either false or true
		boolean h = true;
		System.out.println(h);
		
		// char data types accepts letters both upper case and lower case
		char c = 'c';
		System.out.println(c);
		
		// long data type accept whole and more numbers and we must put "L" by end of numbers before semicolon
		long ab = 453124548754543L;
		System.out.println(ab);
		
		// float accepts only decimal numbers of 6 or 7 digit. 
		float ac = 3.4532f;
		System.out.println(ac);
		
		//integer data type accept whole numbers
		int ad = 45;
		System.out.println(ad);

		
		
		
	}

}
